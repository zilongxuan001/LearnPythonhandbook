# buildings

import turtle as t

t.pencolor("green")
t.left(90)
def main():
    for i in range(10):
        t.fd(10)
        t.right(90)
        t.fd(10)
        t.right(90)
        t.fd(10)
        t.right(90)
        t.fd(10)
        t.right(90)
        t.fd(10)
    t.right(180)
    t.fd(100)
    t.left(90)
    t.fd(10)
    t.left(90)

for i in range(10):
    main()

t.done()
